    boolean thisWorks(String s, String[] a)
    {
        for(int i =0; i < a.length; i++)
        {
            int diff = 0;
            for(int j = 0; j < s.length(); j++)
            {
                if(s.charAt(j) != a[i].charAt(j))
                {
                    diff++;
                }
                
            }
            if(diff != 1)
            {
                return false;
            }
        }
        return true;
    }
    
    
