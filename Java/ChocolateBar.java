    public int maxLength(String letters) {
        int res = 0;
        int N = letters.length();     
        for(int left =0; left < N; left++)
        {
        	for(int right = left; right <= N; right++)
        	{
        		boolean ok = true;
        		int[] a = new int[26];
        		
        		for(char c : letters.substring(left, right).toCharArray())
        		{
        			a[c - 'a']++;
        			if(a[c-'a'] > 1)
        			{
        				ok = false;
        			}
        		}
        		if(ok)
        		{
        			res = Math.max(res, right - left );       					
        		}
        	}     	
        }
        
        return res;
    }

