    public String minNumber(String num) {
        String res = num;
        
        char[] p = num.toCharArray();

        for(int i = 0; i < num.length(); i++)
    	{
    		for(int j = 0; j < num.length(); j++)
    		{
    			char tmp = p[i];
    	    	p[i] = p[j];
    	    	p[j] = tmp;  	
    	    	
    	    	if(p[0] != '0' && IsLess(p, res))
    	    		res = new String(p);	
    			
    			tmp = p[i];
    	    	p[i] = p[j];
    	    	p[j] = tmp;  
    			
    		}
    		
    	}
       
        return res;
    }

    public boolean IsLess(char[] num, String res)
    {   	
    	for(int i =0; i < num.length; i++)
    	{
    		if((num[i] - '0') < (res.charAt(i) - '0'))
    		{
    			return true;	
    		}
    		else if((num[i] - '0') > (res.charAt(i) - '0'))
    		{
    			return false;
    		}  		
    	}    	    	
    	return false;
    }
    
